package com.example.pakaianbagus.models.stockopname;

public class Details {
    private String type;
    private String m_item_id;
    private String m_category_id;
    private String article_code;
    private String size_code;
    private String qty;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getM_item_id() {
        return m_item_id;
    }

    public void setM_item_id(String m_item_id) {
        this.m_item_id = m_item_id;
    }

    public String getM_category_id() {
        return m_category_id;
    }

    public void setM_category_id(String m_category_id) {
        this.m_category_id = m_category_id;
    }

    public String getArticle_code() {
        return article_code;
    }

    public void setArticle_code(String article_code) {
        this.article_code = article_code;
    }

    public String getSize_code() {
        return size_code;
    }

    public void setSize_code(String size_code) {
        this.size_code = size_code;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }
}
