package com.example.pakaianbagus.models;

public class StokToko {
    private String id_stok_toko;
    private String id_artikel;
    private String nama_barang;
    private String path;
    private String qty;
    private String barcode;
    private String no_artikel;
    private String gambar;
    private String total_barang;
    private String harga;
    private String gambar_utama;

    public String getId_stok_toko() {
        return id_stok_toko;
    }

    public void setId_stok_toko(String id_stok_toko) {
        this.id_stok_toko = id_stok_toko;
    }

    public String getId_artikel() {
        return id_artikel;
    }

    public void setId_artikel(String id_artikel) {
        this.id_artikel = id_artikel;
    }

    public String getNama_barang() {
        return nama_barang;
    }

    public void setNama_barang(String nama_barang) {
        this.nama_barang = nama_barang;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getBarcode() {
        return barcode;
    }

    public void setBarcode(String barcode) {
        this.barcode = barcode;
    }

    public String getNo_artikel() {
        return no_artikel;
    }

    public void setNo_artikel(String no_artikel) {
        this.no_artikel = no_artikel;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

    public String getTotal_barang() {
        return total_barang;
    }

    public void setTotal_barang(String total_barang) {
        this.total_barang = total_barang;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }

    public String getGambar_utama() {
        return gambar_utama;
    }

    public void setGambar_utama(String gambar_utama) {
        this.gambar_utama = gambar_utama;
    }
}
